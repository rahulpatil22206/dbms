CREATE TABLE PERSON

(

DRIVER_ID VARCHAR2(10),

NAME VARCHAR2(20),

ADDRESS VARCHAR2(15),

PRIMARY KEY(DRIVER_ID)

);
COMMIT;
CREATE TABLE CAR

(

REGNO VARCHAR2(10),

MODEL VARCHAR2(10),

YEAR NUMBER(4),

PRIMARY KEY(REGNO)

);
COMMIT;
CREATE TABLE ACCIDENT

(

REPORT_NO NUMBER(5),

ADATE DATE,

LOCATION VARCHAR2(15),

PRIMARY KEY(REPORT_NO)

);
COMMIT;
CREATE TABLE OWNS

(

DRIVER_ID VARCHAR2(10),

REGNO VARCHAR2(10),

PRIMARY KEY(DRIVER_ID,REGNO),

FOREIGN KEY(DRIVER_ID) REFERENCES PERSON(DRIVER_ID),

FOREIGN KEY(REGNO) REFERENCES CAR(REGNO)

);
COMMIT;
CREATE TABLE PARTICIPATED

(

DRIVER_ID VARCHAR2(10),

REGNO VARCHAR2(10),

REPORT_NO NUMBER(5),

DAMAGE_AMT NUMBER(7,2),

PRIMARY KEY(DRIVER_ID,REGNO,REPORT_NO),

FOREIGN KEY(DRIVER_ID,REGNO) REFERENCES OWNS(DRIVER_ID,REGNO) ON DELETE CASCADE,

FOREIGN KEY(REPORT_NO) REFERENCES ACCIDENT(REPORT_NO) ON

DELETE CASCADE

);
COMMIT;
